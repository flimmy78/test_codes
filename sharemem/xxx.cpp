std::string getTimebString(const time_t * tb)
{
	char timeBuffer[64] = {0};
	struct tm * timeTm = 0;
	timeTm = gmtime(tb);
	strftime(timeBuffer, 64, "%Y-%m-%d %H:%M:%S", timeTm);
	return std::string(timeBuffer);
}

